package com.lagou.phase01.module03.code.task01;

public class MathTest {

    public static void main(String[] args) {

        System.out.println("max: " + Math.max(10, 20));
        System.out.println("min: " + Math.min(10, 20));
        System.out.println("power: " + Math.pow(2, 3));
        System.out.println("abs: " + Math.abs(-5));
        System.out.println("round: " + Math.round(3.14));
        System.out.println("sqrt: " + Math.sqrt(16));
        System.out.println("random: " + Math.random());
    }
}
